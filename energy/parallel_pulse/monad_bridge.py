"""Monad blockchain bridge for ParallelPulse.

Translates FaultSignal objects from the Python simulation layer into
signed Ethereum transactions sent to the EnergyMarket contract on Monad.

Platform note: Must run on Linux/macOS/WSL2 (not native Windows).
"""
from __future__ import annotations

import logging
import os
import time
from typing import Any

from web3 import Web3
from web3.contract import Contract
from web3.types import TxReceipt

from .schemas import FaultSignal, SOCReading

logger = logging.getLogger(__name__)

